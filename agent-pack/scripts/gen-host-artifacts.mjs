#!/usr/bin/env node
/**
 * Generates host-specific install artifacts from rendered skill content.
 *
 * Outputs:
 *   generated/claude/CLAUDE.md - drop into repo root or merge with existing CLAUDE.md
 *   generated/codex/AGENTS.md - drop into repo root or merge with existing AGENTS.md
 *   generated/copilot/copilot-instructions.md - copy to .github/copilot-instructions.md
 *
 * Usage:
 *   node scripts/gen-host-artifacts.mjs           # write
 *   node scripts/gen-host-artifacts.mjs --check   # verify up-to-date
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(import.meta.dirname, '..');
const CHECK = process.argv.includes('--check');

function read(rel) {
  return fs.readFileSync(path.join(ROOT, rel), 'utf8');
}

function write(rel, content, { check = CHECK, root = ROOT } = {}) {
  const full = path.join(root, rel);
  if (check) {
    const current = fs.existsSync(full) ? fs.readFileSync(full, 'utf8') : '';
    if (current !== content) {
      console.error(`${rel} is stale; run npm run gen:hosts from agent-pack/`);
      process.exitCode = 1;
    }
    return;
  }
  fs.mkdirSync(path.dirname(full), { recursive: true });
  fs.writeFileSync(full, content);
}

function stripFrontmatter(content) {
  if (!content.startsWith('---\n')) return content;
  const end = content.indexOf('\n---', 4);
  if (end === -1) return content;
  return content.slice(end + 4).replace(/^\n/, '');
}

function buildAgentsSection() {
  const agentDirs = [
    path.join(ROOT, 'agents'),
  ];
  const lines = ['## Role-Based Agents', ''];
  const entries = [];
  for (const agentsDir of agentDirs) {
    if (!fs.existsSync(agentsDir)) continue;
    for (const name of fs.readdirSync(agentsDir).sort()) {
      const tmplPath = path.join(agentsDir, name, 'SKILL.md.tmpl');
      if (!fs.existsSync(tmplPath)) continue;
      const content = fs.readFileSync(tmplPath, 'utf8');
      const descMatch = content.match(/^description:\s*\|?\s*\n((?:[ \t]+.+\n?)*)/m);
      const desc = descMatch
        ? descMatch[1].replace(/^[ \t]{2}/gm, '').trim().split('\n')[0]
        : '';
      entries.push({ name, desc });
    }
  }
  entries.sort((a, b) => a.name.localeCompare(b.name));
  for (const { name, desc } of entries) {
    lines.push(`- \`/${name}\` - ${desc}`);
  }
  lines.push('');
  return lines.join('\n');
}

function routingSection(rootSkill) {
  const stripped = stripFrontmatter(rootSkill);
  const start = stripped.indexOf('## Routing');
  if (start === -1) return stripped;
  const next = stripped.indexOf('\n## ', start + 1);
  return next === -1 ? stripped.slice(start) : stripped.slice(start, next);
}

